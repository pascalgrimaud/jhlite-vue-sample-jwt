import { RestLogin, toRestLogin } from '@/common/secondary/RestLogin';
import { Login } from '@/common/domain/Login';

describe('RestLogin', () => {
  it('should convert to RestLogin', () => {
    const login: Login = { username: 'username', password: 'password', rememberMe: true };

    expect(toRestLogin(login)).toEqual<RestLogin>({ username: 'username', password: 'password', rememberMe: true });
  });
});
